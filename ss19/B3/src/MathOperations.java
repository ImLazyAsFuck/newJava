public class MathOperations {

    public static float div(int a, int b){
        return b == 0? (float)a : (float)a/b;
    }


    public static int multiply(int a, int b){
        return a*b;
    }


    public static int subtract(int a, int b){
        return a-b;
    }


    public static int add(int a, int b){
        return a+b;
    }
}