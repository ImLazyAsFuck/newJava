public class MathOperations{
    static int add(int a, int b){
        return a + b;
    }

    static int subtract(int a, int b){
        return a - b;
    }

    static int multiply(int a, int b){
        return a * b;
    }

    static float divide(int a, int b){
        return (float)a / b;
    }
}